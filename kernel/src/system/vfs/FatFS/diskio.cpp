/*-----------------------------------------------------------------------*/
/* Read Sector(s)                                                        */
/*-----------------------------------------------------------------------*/

DRESULT disk_read (
	BYTE pdrv,		/* Physical drive nmuber to identify the drive */
	BYTE *buff,		/* Data buffer to store read data */
	LBA_t sector,	/* Start sector in LBA */
	UINT count		/* Number of sectors to read */
)
{
	DRESULT ret = RES_OK;
	for(int i = 0; i < count; i++)
		if(System::diskManager->allDisks[pdrv]->ReadSector(sector + i, buff + i*512) != 0)
			ret = RES_ERROR;
	
	return ret;
}



/*-----------------------------------------------------------------------*/
/* Write Sector(s)                                                       */
/*-----------------------------------------------------------------------*/

#if FF_FS_READONLY == 0

DRESULT disk_write (
	BYTE pdrv,			/* Physical drive nmuber to identify the drive */
	const BYTE *buff,	/* Data to be written */
	LBA_t sector,		/* Start sector in LBA */
	UINT count			/* Number of sectors to write */
)
{
	DRESULT ret = RES_OK;
	for(int i = 0; i < count; i++)
		if(System::diskManager->allDisks[pdrv]->WriteSector(sector + i, (uint8_t*)buff + i*512) != 0)
			ret = RES_ERROR;
	
	return ret;
}

#endif


